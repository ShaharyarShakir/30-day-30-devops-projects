import { Card, CardHeader, CardTitle, CardContent } from "../../../components/ui/card";
import { Progress } from "../../../components/ui/progress";
import { CheckCircle, ShieldAlert } from "lucide-react";

type Props = {
  prediction: "NORMAL" | "PNEUMONIA";
  confidence: number;
};

export function PredictionCard({ prediction, confidence }: Props) {
  const isNormal = prediction === "NORMAL";

  return (
    <Card className="border-slate-800 bg-[#0f172a]/60 backdrop-blur-md overflow-hidden shadow-xl shadow-black/30">
      <CardHeader className="border-b border-slate-800/60 pb-4">
        <CardTitle className="text-lg font-bold text-slate-200">Diagnostic Analysis</CardTitle>
      </CardHeader>
      <CardContent className="pt-6 space-y-6">
        {/* Label Badge */}
        <div className="flex items-center space-x-4 bg-slate-950/40 p-4 rounded-2xl border border-slate-900">
          <div className={isNormal ? "text-green-500" : "text-red-500"}>
            {isNormal ? <CheckCircle className="h-8 w-8" /> : <ShieldAlert className="h-8 w-8" />}
          </div>
          <div>
            <p className="text-xs text-slate-500 uppercase tracking-wider font-semibold">Prediction Label</p>
            <p className={`text-2xl font-black ${isNormal ? "text-green-400" : "text-red-400"}`}>
              {prediction}
            </p>
          </div>
        </div>

        {/* Confidence Percentage */}
        <div className="space-y-2">
          <div className="flex justify-between items-end text-sm">
            <span className="text-slate-400 font-medium">Model Confidence</span>
            <span className={`font-bold text-base ${isNormal ? "text-green-400" : "text-red-400"}`}>
              {(confidence * 100).toFixed(2)}%
            </span>
          </div>

          <Progress
            value={confidence * 100}
            className={`h-2.5 bg-slate-800 rounded-full [&>div]:transition-all [&>div]:duration-1000 ${
              isNormal
                ? "[&>div]:bg-gradient-to-r [&>div]:from-emerald-500 [&>div]:to-green-400"
                : "[&>div]:bg-gradient-to-r [&>div]:from-orange-500 [&>div]:to-red-500"
            }`}
          />
        </div>

        {/* Radiologist Note */}
        <div className="text-xs text-slate-500 bg-slate-950/20 p-4 rounded-xl border border-slate-800/40 space-y-1">
          <p className="font-semibold text-slate-450">Physician Notice:</p>
          <p>
            This output is generated by an artificial intelligence model using DenseNet121 and decision thresholds. All diagnostic results should be validated by certified radiologists.
          </p>
        </div>
      </CardContent>
    </Card>
  );
}
